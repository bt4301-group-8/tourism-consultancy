    def get_visitors(self, country_name: str):
        """
        Filters the historical data by country name and returns a JSON string
        of monthly visitors sorted by date.
        """
        filtered = [entry for entry in self.historical_data if entry['country'].lower() == country_name.lower()]
        if not filtered:
            raise ValueError(f"No visitor data found for country: {country_name}")

        df = pd.DataFrame(filtered)
        df['month_year'] = pd.to_datetime(df['month_year'])
        df = df.sort_values('month_year').reset_index(drop=True)
        
        return df.to_json(orient="records", date_format="iso")
